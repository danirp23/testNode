const parseService = (polizas, totalPayAll, totalPremium, policiesqy, fileIdentifier) => {

    const ranks = [
        { "rangoA": 1, "rangoB": 45, color: "#1e1045" },
        { "rangoA": 46, "rangoB": 60, color: "#037b86" },
        { "rangoA": 61, "rangoB": 75, color: "#ffd000" },
        { "rangoA": 76, "rangoB": 89, color: "#EE7000" },
        { "rangoA": 90, "rangoB": Infinity, color: "#e92243" }
    ];
    if (!Array.isArray(polizas)) polizas = [polizas];
    let recibos = polizas.reduce((acumulate, element) => {
        (element.recibo.length ? element.recibo : [element.recibo]).forEach((ele) => {
            let numeroPoliza = ele.numeroPoliza;
            ele.type = "Individual"
            if (ele.numeroCertificado !== 0) ele.type = "Colectiva";
            ranks.forEach(rank => {
                if (ele.edadCartera >= rank.rangoA && ele.edadCartera <= rank.rangoB) {
                    acumulate[`${numeroPoliza}>${rank.rangoB}`] = [...(acumulate[`${numeroPoliza}>${rank.rangoB}`] || []), ele];
                }
            })
        })
        return acumulate
    }, {});
    const recibosAsArray = Object.keys(recibos);
    const result = recibosAsArray.map(element => {
        let [recibo, rango] = element.split('>');
        let daysOverdue = ranks.find(rank => Number(rango) === rank.rangoB);
        let taker = recibos[`${element}`][0].tomador.nombre;
        let referencia1 = recibos[`${element}`][0].referencia1
        let referencia2 = recibos[`${element}`][0].referencia2
        let identifier = {
            "value": ((referencia1 || referencia2) || '-')
        }
        if (identifier.value !== '-') identifier.type = (referencia1 != 0 || !referencia1) ? 'Contrato' : 'Placa';
        return {
            product: {
                value: recibo,
                type: recibos[`${element}`][0].ramoProducto.lineaNegocio
            },
            totalPayAll,
            totalPremium,
            type: recibos[`${element}`][0]['type'],
            policiesqy,
            taker,
            daysOverdue,
            identifier,
            ...totalAccounts(recibos[`${element}`]),
            ...minorDate(recibos[`${element}`]),
            receipts: JSON.parse(JSON.stringify(receips(recibos[`${element}`]))),
            fileIdentifier
        }
    });
    return result;
}
const totalAccounts = data => data.reduce((acumulate, element) => {
    acumulate['bonus'] = (acumulate['bonus'] || 0) + (element.recibo?.valorPrimaNeta || element.valorPrimaNeta);
    acumulate['totalPay'] = (acumulate['totalPay'] || 0) + (element.recibo?.valorTotalPendiente || element.valorTotalPendiente);
    return acumulate
}, {});
const minorDate = data => {
    let dates = data.reduce((acumulate, element) => {
        let fechaInicioVigencia = new Date(element.fechaInicioVigencia || element.recibo.fechaInicioVigencia);
        let fechaExpedicion = new Date(element.fechaExpedicion || element.recibo.fechaExpedicion);
        acumulate['begginValidity'] = (new Date(acumulate['begginValidity']) < fechaInicioVigencia) ? acumulate['begginValidity'] : fechaInicioVigencia;
        acumulate['expedition'] = (new Date(acumulate['expedition']) < fechaExpedicion) ? acumulate['expedition'] : fechaExpedicion;
        return acumulate
    }, {
        begginValidity: new Date(),
        expedition: new Date()
    });

    return {
        begginValidity: moment(dates.begginValidity).format("DD/MMM/YYYY"),
        expedition: moment(dates.expedition).format("DD/MMM/YYYY")
    }

}
const receips = data => data.map(element => {
    let identifier = {
        "value": ((element.referencia1 || element.referencia2) || '-')
    }
    if (identifier.value !== '-') identifier.type = (element.referencia1 != 0 || !element.referencia1) ? 'Contrato' : 'Placa';
    
    return {
        certificateNumber: element.numeroCertificado,
        identifier,
        bonus: element.valorPrimaNeta,
        daysOverdue: element.edadCartera,
        begginValidity: moment(element.fechaInicioVigencia).format("DD/MMM/YYYY"),
        expedition: moment(element.fechaExpedicion).format("DD/MMM/YYYY"),
        TotalPay: element.valorTotalPendiente

    }
})
